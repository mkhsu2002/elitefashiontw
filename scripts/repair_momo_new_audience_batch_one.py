def clean_product_text(article: dict[str, Any], brand_reasons: dict[str, str]) -> None:
    products = [*(article.get("mainProducts") or []), *(article.get("sidebarProducts") or [])]
    for product in products:
        merchant_id = product.get("merchantId", "")
        product["selectionReason"] = brand_reasons.get(
            merchant_id,
            "可依商品頁標示、尺寸與實際使用情境比較。",
        )
        product["riskNote"] = "價格、規格、活動與庫存請以下單前商品頁公告為準。"
    for brand in article.get("featuredBrands") or []:
        merchant_id = brand.get("merchantId", "")
        brand["role"] = "品牌參考"
        brand["reason"] = brand_reasons.get(merchant_id, brand.get("reason", "可依商品頁資訊比較。"))


def rewrite_article(slug: str, update: dict[str, Any], config: dict[str, Any], categories: dict[str, pipeline.CategoryConfig]) -> None:
    article_path = ROOT / config["paths"]["generatedArticlesDir"] / f"{slug}.json"
    article = pipeline.load_json(article_path)
    article["excerpt"] = update["excerpt"]
    article["listingExcerpt"] = update["listingExcerpt"]
    article["metaDescription"] = update["metaDescription"]
    article["intro"] = update["intro"]
    article["sections"] = update["sections"]
    article["cta"]["text"] = update["cta"]["text"]
    article["cta"]["variant"] = update["cta"]["variant"]
    if article.get("inlineCtas"):
        article["inlineCtas"][0]["eyebrow"] = update["inline"]["eyebrow"]
        article["inlineCtas"][0]["heading"] = update["inline"]["heading"]
        article["inlineCtas"][0]["text"] = update["inline"]["text"]
    clean_product_text(article, update["brandReasons"])
    article["markdownBody"] = pipeline.render_markdown_body(article)
    pipeline.write_json(article_path, article)
    pipeline.write_text(ROOT / config["paths"]["generatedArticlesDir"] / f"{slug}.md", article["markdownBody"])
    html_output = pipeline.render_article_html(article, config, categories)
    pipeline.assert_frontend_copy(html_output, slug)
    html_output = "\n".join(line.rstrip() for line in html_output.splitlines()) + "\n"
    pipeline.write_text(ROOT / article["file"], html_output)


def main() -> int:
    config, categories = pipeline.load_config()
    for slug in SLUGS:
        rewrite_article(slug, UPDATES[slug], config, categories)
    pipeline.rebuild_outputs(config, categories)
    pipeline.verify_outputs(config, categories)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
