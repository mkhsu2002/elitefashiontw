def reset_existing_records(config: dict[str, Any], slugs: set[str]) -> None:
    publish_log_path = ROOT / config["paths"]["publishLogJson"]
    publish_log = pipeline.load_json(publish_log_path, default={"version": 1, "entries": []})
    publish_log["entries"] = [
        entry
        for entry in publish_log.get("entries", [])
        if Path(str(entry.get("file", ""))).stem not in slugs
    ]
    publish_log["updatedAt"] = pipeline.now_iso()
    pipeline.write_json(publish_log_path, publish_log)

    authenticity_log_path = ROOT / "automation" / "content-authenticity-log.json"
    authenticity_log = pipeline.load_json(authenticity_log_path, default={"version": 1, "entries": []})
    authenticity_log["entries"] = [
        entry for entry in authenticity_log.get("entries", []) if entry.get("slug") not in slugs
    ]
    authenticity_log["updatedAt"] = pipeline.now_iso()
    pipeline.write_json(authenticity_log_path, authenticity_log)


def update_queue(saved_articles: list[dict[str, Any]], config: dict[str, Any]) -> None:
    queue_path = ROOT / config["paths"]["queueJson"]
    queue = pipeline.load_json(queue_path)
    by_key = {(article["queueId"], article["queueOrder"]): article for article in saved_articles}
    for series in queue["series"]:
        for item in series.get("items", []):
            key = (series["queueId"], item.get("order"))
            article = by_key.get(key)
            if not article:
                continue
            item["status"] = "published"
            item["articleId"] = article["id"]
            item["file"] = article["file"]
            item["publishedAt"] = article["publishedAt"]
        if all(item.get("status") == "published" for item in series.get("items", [])):
            series["status"] = "completed"
        elif any(item.get("status") == "published" for item in series.get("items", [])):
            series["status"] = "in_progress"
    queue["updatedAt"] = pipeline.now_iso()
    pipeline.write_json(queue_path, queue)


def update_tracker(saved_articles: list[dict[str, Any]], tracker_rows: list[dict[str, str]], fieldnames: list[str]) -> None:
    by_merchant = {row["merchant_id"]: row for row in tracker_rows}
    mentions: dict[str, list[dict[str, Any]]] = defaultdict(list)
    for article in saved_articles:
        merchant_ids = {
            product["merchantId"]
            for product in [*article.get("mainProducts", []), *article.get("sidebarProducts", [])]
            if product.get("merchantId")
        }
        merchant_ids.update(
            brand["merchantId"] for brand in article.get("featuredBrands", []) if brand.get("merchantId")
        )
        for merchant_id in merchant_ids:
            mentions[merchant_id].append(article)

    for merchant_id, hits in mentions.items():
        row = by_merchant[merchant_id]
        row["coverage_status"] = "live"
        row["article_created"] = "true"
        row["link_status"] = "usable"
        row["risk_notes"] = row.get("risk_notes") or "不使用誇大推薦語氣，商品規格以 momo 商品頁為準"
        slugs = [part for part in row.get("article_slug", "").split(";") if part]
        urls = [part for part in row.get("live_url", "").split(";") if part]
        increment = 0
        for article in hits:
            if article["slug"] not in slugs:
                slugs.append(article["slug"])
                increment += 1
            if article["url"] not in urls:
                urls.append(article["url"])
        row["article_slug"] = ";".join(slugs)
        row["live_url"] = ";".join(urls)
        if increment:
            row["mention_count"] = str(int(row.get("mention_count") or 0) + increment)
        row["last_mentioned_at"] = TODAY
        note = f"{TODAY} 一人生活與喪偶重建系列延伸選物置入。"
        if note not in row.get("notes", ""):
            row["notes"] = (row.get("notes", "").rstrip() + (" " if row.get("notes", "").strip() else "") + note).strip()

    with TRACKER_CSV.open("w", encoding="utf-8-sig", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames, lineterminator="\n")
        writer.writeheader()
        writer.writerows(tracker_rows)


def main() -> int:
    config, categories = pipeline.load_config()
    tracker_rows, rows_by_merchant, fieldnames = load_tracker()
    article_specs = specs()
    if len(article_specs) != len(COVER_SOURCES):
        raise SystemExit(f"Spec/cover count mismatch: {len(article_specs)} != {len(COVER_SOURCES)}")
    missing = sorted(
        {
            merchant_id
            for spec in article_specs
            for merchant_id in spec["products"]
            if merchant_id not in rows_by_merchant
        }
    )
    if missing:
        raise SystemExit(f"Tracker missing merchants: {', '.join(missing)}")

    slugs = {spec["slug"] for spec in article_specs}
    reset_existing_records(config, slugs)

    saved_articles: list[dict[str, Any]] = []
    for spec, cover_source in zip(article_specs, COVER_SOURCES):
        article = build_article(spec, rows_by_merchant, cover_source)
        saved = pipeline.save_generated_article(article, spec["queueId"], config, categories)
        saved["queueOrder"] = spec["order"]
        pipeline.validate_generated_article(saved, config)
        saved["authenticityReview"] = pipeline.run_authenticity_review(saved, config)
        pipeline.write_json(ROOT / config["paths"]["generatedArticlesDir"] / f"{saved['slug']}.json", saved)
        pipeline.write_text(ROOT / config["paths"]["generatedArticlesDir"] / f"{saved['slug']}.md", saved["markdownBody"])
        pipeline.append_publish_log(config, saved, trigger_type=TRIGGER_TYPE, queue_id=spec["queueId"])
        saved_articles.append(saved)

    update_queue(saved_articles, config)
    update_tracker(saved_articles, tracker_rows, fieldnames)
    pipeline.write_json(
        ROOT / config["paths"]["latestRunJson"],
        {
            "version": 1,
            "updatedAt": pipeline.now_iso(),
            "status": "generated",
            "triggerType": TRIGGER_TYPE,
            "newsletter": "not_sent_manual_codex_publish",
            "articleIds": [article["id"] for article in saved_articles],
            "articleSlugs": [article["slug"] for article in saved_articles],
            "notes": "Codex 手動批次撰文與封面生成；直接推上 main 不會自動寄送電子報。",
        },
    )
    pipeline.rebuild_outputs(config, categories)
    pipeline.verify_outputs(config, categories)
    print(f"Generated {len(saved_articles)} solo-living planned articles")
    for article in saved_articles:
        print(f"- {article['slug']} -> {article['file']}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
